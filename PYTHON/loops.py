#Basic syntax for python loops
# Loops is used to iterate over a sequence of numbers

for i in [0, 1, 2, 3, 4, 5]:
    print(i)

# range = generate sequence of numbers

for i in range(3):
    print(i)

# List of name
names = ["Asyikin", "Liam", "Austin", "Anna"]
for name in names:
    print(name)

# characters in name
name = "Asyikin"

for character in name:
    print(character)

    
